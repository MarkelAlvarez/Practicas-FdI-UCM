package ejemploJTablePR5;

public class EventsPrueba {

	private Integer _time;
	private Integer _prioridad;
	
	public EventsPrueba(Integer time, Integer prioridad) {
		_time = time;
		_prioridad = prioridad;
		
	}

	public int getTime() {
		return _time;

	}

	public int getPrioridad() {
		return _prioridad;

	}
	
}
